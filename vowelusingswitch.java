class vowelusingswitch
{ 
 public static void main( String args[] )
  {
    char ch;
	ch='u';
	switch(ch)
	{ 
	    case 'A':
		case 'a':
		case 'E':
		case 'e':
		case 'I':
		case 'i':
		case 'O':
		case 'o':
		case 'U':
		case 'u': System.out.println("given letter is vowel");
		          break;
		default: System.out.println(" Enter valid choice");
	}
   }
}   
		